package com.agendaJogos.models;

public class Ingresso {
    private Integer id_ingresso;
    private Integer id_torcedor;
    private Integer id_jogo;


    public Integer getId_ingresso() {
        return id_ingresso;
    }

    public void setId_ingresso(Integer id_ingresso) {
        this.id_ingresso = id_ingresso;
    }

    public Integer getId_torcedor() {
        return id_torcedor;
    }

    public void setId_torcedor(Integer id_torcedor) {
        this.id_torcedor = id_torcedor;
    }

    public Integer getId_jogo() {
        return id_jogo;
    }

    public void setId_jogo(Integer id_jogo) {
        this.id_jogo = id_jogo;
    }
}
